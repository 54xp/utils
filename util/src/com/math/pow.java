public class pow {
    public static void main(String[] args) {
        double pow = Math.pow(-2, 2);  // 平方
        System.out.println("pow = " + pow);

        double sqrt = Math.sqrt(2);  //开平方
        System.out.println("sqrt = " + sqrt);
    }
}
